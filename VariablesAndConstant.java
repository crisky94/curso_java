public class VariablesAndConstant {
    static void main(String[] args) {

        //Variables

        String name = "Kenay";
        System.out.println(name);

        name = "Kylo";
        System.out.println(name);

        var email = "patata";
        System.out.println(email);


        int age = 31;
        System.out.println(age);

        // Constantes

        //Poner los nombres de las constantes en mayusculas
        final String EMAIL = "patata@patata.com";
        System.out.println(EMAIL);


    }
}
